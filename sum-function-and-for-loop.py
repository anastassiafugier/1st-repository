# Sum function
# Implementation
def sum_of_num(first_num, second_num):
    output = first_num + second_num
    return output


# Usage
output = sum_of_num(13, 42)
print(output)


# For loop
# Implementation
def sum_loop(first_num, second_num):
    arguments = [first_num, second_num]
    num = 0
    for i in arguments:
        num += i
    return num


# Usage
num = sum_loop(2, 38)
print(num)

