# Sum function
# Implementation
def sum_of_num(first_num, second_num):
    output = first_num + second_num
    print(output)
    return output


# Usage
sum_of_num(13, 42)


# For loop
# Implementation
def sum_loop(first_num, second_num):
    arguments = [first_num, second_num]
    num = 0
    for i in arguments:
        num += i
    print(num)
    return num


# Usage
sum_loop(2, 38)
